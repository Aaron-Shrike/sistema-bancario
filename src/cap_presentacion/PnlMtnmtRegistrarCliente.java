    private void btRegClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegClienteActionPerformed
        // TODO add your handling code here:
        try{
            Color negro = new Color(0,0,0);
            Color rojo = new Color(255,51,51);
            java.util.Date fechaActual = new java.util.Date();
            
            //comparar el 'a√±o actual' con el 'a√±o de nacimiento' del cliente para comprobar si es mayor de edad
            if(fechaActual.getYear() - dcFechaNaci.getDate().getYear() >= 18 
                    && tfDni.getForeground().equals(negro)  && tfTelefono.getForeground().equals(negro)){
                Cliente objCli = new Cliente();

                objCli.setDni(tfDni.getText());
                objCli.setNombre(tfNombre.getText());
                objCli.setApellidoPaterno(tfApPaterno.getText());
                objCli.setApellidoMaterno(tfApMaterno.getText());
                    //'fecha' de tipo (java.sql.Date) para ingresar a la BD, inicializado con la fecha obtenida del JDateChooser
                    Date fecha = new Date(dcFechaNaci.getDate().getTime());
                objCli.setFechaNacimiento(fecha);
                objCli.setDireccion(tfDireccion.getText());
                objCli.setTelefono(tfTelefono.getText());

                objCli.agregar();

                JOptionPane.showMessageDialog(this,"Cliente REGISTRADO",
                            "MENSAJE DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE); 

                tfDni.setText(Strings.ejmDni);
                tfDni.setCaretPosition(0);
                tfDni.setForeground(new Color(153,153,153));
                
                tfNombre.setText(Strings.ejmNombre);
                tfNombre.setCaretPosition(0);
                tfNombre.setForeground(new Color(153,153,153));
                
                tfApPaterno.setText(Strings.ejmApPaterno);
                tfApPaterno.setCaretPosition(0);
                tfApPaterno.setForeground(new Color(153,153,153));
                
                tfApMaterno.setText(Strings.ejmApMaterno);
                tfApMaterno.setCaretPosition(0);
                tfApMaterno.setForeground(new Color(153,153,153));
                
                dcFechaNaci.setDate(null);
                
                tfDireccion.setText(Strings.ejmDireccion);
                tfDireccion.setCaretPosition(0);
                tfDireccion.setForeground(new Color(153,153,153));
                
                tfTelefono.setText(Strings.ejmTelefono);
                tfTelefono.setCaretPosition(0);
                tfTelefono.setForeground(new Color(153,153,153));
                
                tfDni.requestFocus();
                
            }else if(tfDni.getForeground().equals(rojo)){
                JOptionPane.showMessageDialog(this,"DNI con LETRAS",
                            "ERROR DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
                
            }else if(fechaActual.getYear() - dcFechaNaci.getDate().getYear() < 18){
                JOptionPane.showMessageDialog(this,"Cliente MENOR DE EDAD",
                            "MENSAJE DEL SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                
            }else if(tfTelefono.getForeground().equals(rojo)){
                JOptionPane.showMessageDialog(this,"Telefono con LETRAS",
                            "ERROR DEL SISTEMA", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(Exception  error){
            JOptionPane.showMessageDialog(this,error.getMessage(),
                        "ERROR DEL SISTEMA", JOptionPane.ERROR_MESSAGE);        
        }
    }//GEN-LAST:event_btRegClienteActionPerformed

